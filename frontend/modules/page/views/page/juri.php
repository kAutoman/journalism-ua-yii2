                    професіоналом. Кожен член Робочої Групи висуває по 3 кандидати в
                    Журі. Кожний з цих кандидатів, у свою чергу, висуває 3 кандидати в
                    Журі.
                </p>
                <p>
                    Так відбувається до тих пір, поки частка нових прізвищ в списку не
                    перевищуватиме 10% від загального числа кандидатур. Далі
                    відбуваеться рейтингове голосування. З повного списку кандидатур
                    відбираються ті, хто зібрав найбільшу кількість голосів.

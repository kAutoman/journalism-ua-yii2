            <div class="number">01.</div>
            <div class="title">Хто може брати участь у конкурсі?</div>
            <div class="text"><p>Подати свій матеріал на конкурс може кожен, хто професійно займається журналістикою в
                    Україні, - без обмежень за віком, типом ЗМІ (преса, телебачення, радіо, інтернет), жанром і сферою
                    журналістики, місцем проживання тощо.</p>
                <p>Висування може бути самостійним (журналіст висуває свої матеріали на конкурс від свого імені),
                    колективним (матеріали на конкурс висуває редакція або видавництво) і від імені третьої особи
                    (хто-небудь, фізична особа або організація висуває на конкурс який-небудь журналістський
                    матеріал).</p>
                <p>Один і той же журналіст, редакція або третя особа можуть подавати матеріали одночасно у кілька основних номінацій.  
Один і той самий матеріал не може бути поданий в декілька номінацій, лише в одну.
На кожен матеріал, поданий на Конкурс, необхідно оформити окрему заявку.
</p>
                <p>Кількість матеріалів у Спеціальні номінації визначається окремо. Детальну інформацію щодо кожної зі Спецномінацій ви можете знайти у вкладці “Критерії”.</p></div>

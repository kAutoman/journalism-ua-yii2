					<p>29 вересня 2022 року під час ХІІІ церемонії національного конкурсу професійної журналістики «Честь Професії» організатори назвали імена переможців. Цьогоріч у 6 основних номінаціях та 2 спецномінаціях змагалося 902 матеріали. До шортлісту основних номінацій увійшло 50 найбільш професійних, впливових та резонансних робіт.</p>
                    <p>Також в основних номінаціях, окрім головного переможця, назвали і найкращі матеріали у локальних медіа. Окрім того, учасники отримали спеціальні призи – «Надія журналістики», «Найкращий аудіоматеріал» та спеціальну номінацію від Наглядової ради «За відданість професії за найтяжчих умов».</p>

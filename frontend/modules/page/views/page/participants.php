                    </li>
                    <li><img src="/img/participants/block_text/icon_4.svg" alt="Text 4"> у разі подання матеріалу іноземною мовою, просимо надсилати переклад матеріалу у текстовому форматі на пошту організаторів - <a href="mailto:project@nam.com.ua">project@nam.com.ua</a>, або прикріпити посилання на переклад в анкеті при її заповненні.
                    </li>
